                        <h2>PRIVACY &amp; POLICY</h2>
<p>" I SQUARE TAXI " values all customers of this site. We value your privacy and trust. This policy condition explains the information we collect from you, how we collect this information, and how we use it. By using this website, you are accepting the practices explained in this policy document. For any clarifications, you may write to support@isquaretaxi.in<br /><strong><br />What information " I SQUARE TAXI " collects from customers?</strong>&nbsp;<br />We collect very basic personal information like name, address, email, phone number. We do not collect any bank account details or credit card information or any other financial data.<br /><strong><br />When does " I SQUARE TAXI " collect this information?</strong>&nbsp;<br />We collect this information while you register with us as a customer. You must register with us if you want to book your travel either online or over the phone.<br /><strong><br />Whether " I SQUARE TAXI " discloses customer&lsquo;s information to third parties?</strong>&nbsp;<br />" I SQUARE TAXI " collects customer&lsquo;s information only for the internal use of processing of bookings and delivery. " I SQUARE TAXI " will not disclose any of the personal information of customers to third parties.<br /><strong><br />Does " I SQUARE TAXI " protect customer&lsquo;s information?</strong>&nbsp;<br />Yes. " I SQUARE TAXI " protect it from unauthorized access or disclosure to the extent possible.</p>
<p><span class="bold">What type of car bookings can I make?</span></p>
<p class="text_part">On&nbsp;" I SQUARE TAXI ", you can book a cab for travelling in a city to another city (for one way or roundtrip). You can book more than one cab depending on your usage.</p>
<p class="policy"><span class="bold">How soon can I get a cab?</span></p>
<p class="text_part">For usage within a city, you must book the cab at least 4 hours before the journey time and for outstation usage, at least 24 hours before the journey time. It is however recommended to book as early as possible for multiple options and competitive rates.</p>
<p class="policy">H<span class="bold">ow should I pay? Is it safe to pay online?</span></p>
<p class="text_part">You can pay with any Visa, Mastercard and American Express credit card or with any major debit card issued in India.&nbsp;" I SQUARE TAXI " offers multiple payment options including all debit/credit cards &amp; net banking. About safety, all payments on&nbsp;" I SQUARE TAXI " are Verisign secured. Besides, all your card details and other sensitive information are stored using advanced encryption techniques. As a precaution, we never store your CVV number without which, your card can not be used for any online payment.</p>
<p class="policy"><span class="bold">What happens after I&rsquo;ve made the payment?</span></p>
<p class="text_part">After receiving your payment, we send you an SMS and/or a confirmation mail, followed by your voucher which you receive with 1 hours of making payment. On the day of the journey, you will get the cab details through SMS at least 4 hours before the travel.</p>
<p class="policy"><span class="bold">How am I charged?</span></p>
<p class="text_part">You make the online payment to&nbsp;" I SQUARE TAXI " which is then transferred to the vendor. Any additional charges like toll, parking and state tax will be payable on the actual basis to the driver/vendor unless mentioned otherwise. In case of extension of plans (in terms of kilometres or hours, extra charges apply as mentioned).</p>
<p class="policy"><span class="bold">What if I want to change or cancel my booking?</span></p>
<p class="text_part">Please call us at 9043-99-66-99 to change or cancel your booking. In case of changed booking, the pick-up address cannot be changed and booking will depend on the availability of desired car type on the desired travel date. Any additional charges, if applicable, will be paid by the customer.</p>
<p class="policy"><span class="bold">How much are the cancellation charges?</span></p>
<p class="text_part">For cancellation made within 24 hours of the journey or in case of no-show, full booking cost will be charged. For cancellation made prior to 24 hours of journey, no cancellation fee will be charged. Any other travel policies, if mentioned by the vendor, will apply.</p>
<p class="policy"><span class="bold">What if I face a problem before or during the journey?</span></p>
<p class="text_part">For any issues with cab, the best way is to contact the driver or the vendor. If that doesn&rsquo;t help, please call&nbsp;" I SQUARE TAXI " at 9043-99-66-99 and we&rsquo;ll do our best to assist you. For all correspondence with " I SQUARE TAXI ", please use your cab booking number given at the time of booking.</p>    
